const THEMES = {
  spring: {
    color: "#f0ffe0",
    pattern: SPRING_PATTERN,
  },
  summer: {
    color: "#e6f7ff",
    pattern: SUMMER_PATTERN,
  },
  autumn: {
    color: "#F1DDCC",
    pattern: AUTUMN_PATTERN,
  },
};

const applyTheme = async () => {
  let theme = THEMES.summer;
  const response = await axios.get(`${BASE_URL}/api/theme-setting`);
  const data = response.data.data;

  if (data) {
    if (data.IsSpring === true) {
      theme = THEMES.spring;
    } else if (data.IsAutumn === true) {
      theme = THEMES.autumn;
    }
  }

  document.body.style.backgroundColor = theme.color;
  document.body.style.backgroundImage = theme.pattern;
};
export { applyTheme };
